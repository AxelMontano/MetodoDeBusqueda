    public void mostrarA(){
        String cad = "";
        int copiaA[] = orden.CopiaA();
        for(int i = 0; i < copiaA.length;i++){
            cad += "[ "+copiaA[i]+" ] ";
        }
        jTextArea1.setText(cad);
    }
